package lesson1.part2;


public class CompanyWorker {
                                private int Age;
                                private int WorkDur;
                                private int YearsExp;
                                private int Salary;
                                private String Position;
                                private String Division;
                                private int Subordinates;
                                private boolean Relation;
                                private double Ownership;
                                private boolean Employment;
public int getAge() {return Age;}
public void setAge (int Age) {
	this.Age = Age;
}
public int getWorkDur() {return WorkDur;}
public void setWorkDur (int WorkDur) {
	this.WorkDur = WorkDur;
}
public int getYearsExp() {return YearsExp;}
public void setYearsExp (int YearsExp) {
	this.YearsExp = YearsExp;
}
public int getSalary() {return Salary;}
public void setSalary (int Salary) {
	this.Salary = Salary;
}
public String getPosition() {return Position;}
public void setPosition (int Position) {
	this.Position = Position;
}
public String getDivision() {return Division;}
public void setDivision (String Division) {
	this.Division = Division;
}
public int getSubordinates() {return Subordinates;}
public void setSubordinates (int Subordinates) {
	this.Subordinates = Subordinates;
}
public boolean getRelation() {return Relation;}
public void setRelation (int Relation) {
	this.Relation = Relation;
}
public double getOwnership() {return Ownership;}
public void setOwnership (double Ownership) {
	this.Ownership = Ownership;
}
public boolean getEmployment() {return Employment;}
public void setEmployment (boolean Employment) {
	this.Employment = Employment;
}

}

