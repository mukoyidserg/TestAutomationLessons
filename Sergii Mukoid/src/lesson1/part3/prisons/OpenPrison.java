
public class OpenPrison extends Prison {
                                private boolean Employment;
                                private String RecreationalFacilities;
                                private String ReintegrationPrograms;
public boolean getEmployment() {return Employment;}
public void setEmployment (boolean Employment) {
	this.Employment = Employment;
} 
public String getRecreationalFacilities() {return RecreationalFacilities;}
public void setRecreationalFacilities (String RecreationalFacilities) {
	this.RecreationalFacilities = RecreationalFacilities;
}
public String getReintegrationPrograms() {return ReintegrationPrograms;}
public void setReintegrationPrograms (String ReintegrationPrograms) {
	this.ReintegrationPrograms = ReintegrationPrograms;
}

}
